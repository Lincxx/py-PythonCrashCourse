fruits = ['banana', 'watermelon', 'kiwi', 'mango']

if 'banana' in fruits:
    print('You really like banana')
if 'strawberry' in fruits:
    print('You really like strawberry')
if 'kiwi' in fruits:
    print('You really like kiwi')
if 'mango' in fruits:
    print('You really like mango')
if 'dates' in fruits:
    print('You really like dates')